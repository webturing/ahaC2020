#include<stdio.h>
int main() {
	//逻辑运算符优先级<比较运算符 <算术运算符 
	int a=3;
	//逻辑AND 且  1&&1=1 0&&*=0  
	printf("%d\n",5+7>4*2 && 4>3); //1 
	printf("%d\n",5+7>4*2 && 4<3); //0 
	printf("%d\n",4<3 && 4<3*9); //0 
	//逻辑OR 或  0||0=0  1||*=1
	printf("%d\n",5+7>4*2 || 4>3); //1
	printf("%d\n",5+7>4*2 || 4<3); //1 
	printf("%d\n",4<3 || 4<3*9); //1
	printf("%d\n",4<3 || 4>3*9); //0
	
	//逻辑非NOT  !1=0 !0=1
	printf("%d\n",!(4<3)); 
	printf("%d\n",!!!!!(4<3)); 
	return 0;
}
